function SocialLink({
  href,
  label,
  children,
}: {
  href: string;
  label: string;
  children: React.ReactNode;
}) {
  const [hover, setHover] = useState(false);
  return (
    <a
      href={href}
      target="_blank"
      rel="noopener noreferrer"
      aria-label={label}
      title={label}
      onMouseEnter={() => setHover(true)}
      onMouseLeave={() => setHover(false)}
      style={{
        width: "2rem",
        height: "2rem",
        display: "inline-flex",
        alignItems: "center",
        justifyContent: "center",
        borderRadius: "999px",
        color: hover ? "var(--label-primary)" : "var(--label-tertiary)",
        transition: "color 160ms ease, transform 160ms ease",
        transform: hover ? "scale(1.08)" : "scale(1)",
      }}
    >
      {children}
    </a>
  );
}

